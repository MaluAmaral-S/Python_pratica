n1 = int(input('diga um numero'))
operador = str(input('operador'))
n2 = int(input('numero'))

if operador == 'x':
    resultado = n1 * n2
    print(resultado)


if operador == '/':
    resultado = n1 / n2
    print(resultado)

if operador == '+':
    resultado = n1 + n2
    print (resultado)

if  operador == '-':
    resultado = n1 - n2
    print(resultado)

